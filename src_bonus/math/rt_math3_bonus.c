static void	mat4_mult_r01(t_mat4 *r, t_mat4 a, t_mat4 b)
{
	r->m[0][0] = a.m[0][0] * b.m[0][0] + a.m[0][1] * b.m[1][0] + a.m[0][2]
		* b.m[2][0] + a.m[0][3] * b.m[3][0];
	r->m[0][1] = a.m[0][0] * b.m[0][1] + a.m[0][1] * b.m[1][1] + a.m[0][2]
		* b.m[2][1] + a.m[0][3] * b.m[3][1];
	r->m[0][2] = a.m[0][0] * b.m[0][2] + a.m[0][1] * b.m[1][2] + a.m[0][2]
		* b.m[2][2] + a.m[0][3] * b.m[3][2];
	r->m[0][3] = a.m[0][0] * b.m[0][3] + a.m[0][1] * b.m[1][3] + a.m[0][2]
		* b.m[2][3] + a.m[0][3] * b.m[3][3];
	r->m[1][0] = a.m[1][0] * b.m[0][0] + a.m[1][1] * b.m[1][0] + a.m[1][2]
		* b.m[2][0] + a.m[1][3] * b.m[3][0];
	r->m[1][1] = a.m[1][0] * b.m[0][1] + a.m[1][1] * b.m[1][1] + a.m[1][2]
		* b.m[2][1] + a.m[1][3] * b.m[3][1];
	r->m[1][2] = a.m[1][0] * b.m[0][2] + a.m[1][1] * b.m[1][2] + a.m[1][2]
		* b.m[2][2] + a.m[1][3] * b.m[3][2];
	r->m[1][3] = a.m[1][0] * b.m[0][3] + a.m[1][1] * b.m[1][3] + a.m[1][2]
		* b.m[2][3] + a.m[1][3] * b.m[3][3];
}

static void	mat4_mult_r23(t_mat4 *r, t_mat4 a, t_mat4 b)
{
	r->m[2][0] = a.m[2][0] * b.m[0][0] + a.m[2][1] * b.m[1][0] + a.m[2][2]
		* b.m[2][0] + a.m[2][3] * b.m[3][0];
	r->m[2][1] = a.m[2][0] * b.m[0][1] + a.m[2][1] * b.m[1][1] + a.m[2][2]
		* b.m[2][1] + a.m[2][3] * b.m[3][1];
	r->m[2][2] = a.m[2][0] * b.m[0][2] + a.m[2][1] * b.m[1][2] + a.m[2][2]
		* b.m[2][2] + a.m[2][3] * b.m[3][2];
	r->m[2][3] = a.m[2][0] * b.m[0][3] + a.m[2][1] * b.m[1][3] + a.m[2][2]
		* b.m[2][3] + a.m[2][3] * b.m[3][3];
	r->m[3][0] = a.m[3][0] * b.m[0][0] + a.m[3][1] * b.m[1][0] + a.m[3][2]
		* b.m[2][0] + a.m[3][3] * b.m[3][0];
	r->m[3][1] = a.m[3][0] * b.m[0][1] + a.m[3][1] * b.m[1][1] + a.m[3][2]
		* b.m[2][1] + a.m[3][3] * b.m[3][1];
	r->m[3][2] = a.m[3][0] * b.m[0][2] + a.m[3][1] * b.m[1][2] + a.m[3][2]
		* b.m[2][2] + a.m[3][3] * b.m[3][2];
	r->m[3][3] = a.m[3][0] * b.m[0][3] + a.m[3][1] * b.m[1][3] + a.m[3][2]
		* b.m[2][3] + a.m[3][3] * b.m[3][3];
}

t_mat4	mat4_mat4_mult(t_mat4 m1, t_mat4 m2)
{
	t_mat4	res;

	ft_memset(&res, 0, sizeof(t_mat4));
	mat4_mult_r01(&res, m1, m2);
	mat4_mult_r23(&res, m1, m2);
	return (res);
}

t_mat4	mat4_transpose(t_mat4 m)
{
	t_mat4	res;

	res.m[0][0] = m.m[0][0];
	res.m[0][1] = m.m[1][0];
	res.m[0][2] = m.m[2][0];
	res.m[0][3] = m.m[3][0];
	res.m[1][0] = m.m[0][1];
	res.m[1][1] = m.m[1][1];
	res.m[1][2] = m.m[2][1];
	res.m[1][3] = m.m[3][1];
	res.m[2][0] = m.m[0][2];
	res.m[2][1] = m.m[1][2];
	res.m[2][2] = m.m[2][2];
	res.m[2][3] = m.m[3][2];
	res.m[3][0] = m.m[0][3];
	res.m[3][1] = m.m[1][3];
	res.m[3][2] = m.m[2][3];
	res.m[3][3] = m.m[3][3];
	return (res);
}
